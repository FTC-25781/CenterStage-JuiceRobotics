@TeleOp(name = "Concept: AprilTag", group = "Concept")
public class AprilTagsRelocalizationTest extends LinearOpMode {
    Robot robot = new Robot(hardwareMap, false);

    @Override
    public void runOpMode() {
        // Wait for the DS start button to be touched.
        telemetry.addData("DS preview on/off", "3 dots, Camera Stream");
        telemetry.addData(">", "Touch Play to start OpMode");
        telemetry.update();
        waitForStart();

        if (opModeIsActive()) {
            while (opModeIsActive()) {
                Pose2d reloc = robot.relocalization.relocalizeUsingBackdrop(robot.drive.getPoseEstimate());
                robot.drive.setPoseEstimate(reloc);

                telemetry.addLine("x: " + reloc.getX());
                telemetry.addLine("y: " + reloc.getY());
                telemetry.addLine("heading: " + reloc.getHeading());
                telemetry.update();
            }
        }
    }
}